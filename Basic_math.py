def main():
    print("2+3={}".format(2 + 3))
    print("5-4={}".format(5 - 4))
    print("8/3={}".format(8 / 3))
    print("8/3={}".format(8 / 3))
    print("3^2={}".format(3 ^ 2))  # bitwise operator
    print("16**0.5={}".format(16**0.5))


if __name__ == "__main__":
    main()
